SELECT CNH.DATA AS DATACTRC
        ,MNF.DATA AS DATAMNF
        ,MNF.CHEGADA AS CHEGADAMNF
        ,IME.MEG_CODIGO AS CODIGOMEG
        ,MEG.DATA AS DATAMEG
        ,MEG.TIPOENTREGA AS TIPOENTREGA
        ,MNF.DTINICIODESCARGA AS INICIODESCARGA
        ,MNF.DTFINALDESCARGA AS FINALDESCARGA
        ,CNH.DATAENTREGA AS DATAENTREGA,
        UPPER( ISNULL(IME.RECEBEDOR,'ENCARREGADO RESPONS√ÅVEL') ) AS NOMERECEBEDOR,
        'N.INFO'         AS DOCUMENTORECEBEDOR
    FROM SIC.dbo.CONFIRMAFACIL CF
    JOIN CARGASSQL.dbo.CNH ON CNH.EMP_CODIGO=SUBSTRING(CF.CTRC,1,3) AND CNH.SERIE = SUBSTRING(CF.CTRC,4,1) AND CNH.CTRC = SUBSTRING(CF.CTRC,5,10)
    LEFT JOIN CARGASSQL.dbo.TRB ON CNH.EMP_CODIGO = TRB.EMP_CODIGO_CNH AND CNH.SERIE      = TRB.CNH_SERIE	AND CNH.CTRC           = TRB.CNH_CTRC
    LEFT JOIN CARGASSQL.dbo.MNF ON TRB.EMP_CODIGO = MNF.EMP_CODIGO	 AND TRB.MNF_CODIGO = MNF.CODIGO
    LEFT JOIN CARGASSQL.dbo.IME ON IME.CNH_CTRC   = CNH.CTRC	         AND IME.CNH_SERIE  = CNH.SERIE	    AND IME.EMP_CODIGO_CNH = CNH.EMP_CODIGO
    LEFT JOIN CARGASSQL.dbo.MEG ON MEG.CODIGO     = IME.MEG_CODIGO	 AND MEG.EMP_CODIGO = IME.EMP_CODIGO
    WHERE 
	CF.FASE_ID = 3