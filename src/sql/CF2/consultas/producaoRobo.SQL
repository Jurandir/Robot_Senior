SELECT CF.FASE_ID,FA.FASE,COUNT(CF.ID) QTDE,MAX(CF.DT_UPDATE) ULTIMO_UPDATE
FROM SIC.dbo.CONFIRMAFACIL CF
LEFT JOIN SIC.dbo.CONFIRMAFACILFASE FA ON FA.ID = CF.FASE_ID
GROUP BY CF.FASE_ID,FA.FASE
ORDER BY CF.FASE_ID;


SELECT CO.OCORRENCIA_ID,CO.OCORRENCIA_NOME,COUNT(CO.ID) TOTAL,SUM(CASE WHEN FLAG_SEND=0 THEN 1 ELSE 0 END) PENDENTES,
SUM(CASE WHEN FLAG_SEND=2 THEN 1 ELSE 0 END) ERROS,
SUM(CASE WHEN FLAG_SEND=1 THEN 1 ELSE 0 END) SUCESSO,
MAX(CO.DT_CREATE) ULT_OCORRENCIA_GERADA
FROM SIC.dbo.CONFIRMAFACILOCORRENCIA CO
JOIN SIC.dbo.CONFIRMAFACIL CF ON CF.ID = CO.CONFIRMAFACIL_ID
GROUP BY CO.OCORRENCIA_ID,CO.OCORRENCIA_NOME
ORDER BY CO.OCORRENCIA_ID;