SELECT CTRC 
  FROM SIC..CONFIRMAFACIL CF 
 WHERE FASE_ID BETWEEN 4 AND 6
   AND EXISTS (SELECT 1 FROM SIC..CONFIRMAFACILOCORRENCIA OC WHERE OC.CONFIRMAFACIL_ID = CF.ID AND OC.OCORRENCIA_ID = 999 AND OC.FLAG_SEND = 0) 
   AND FLAG_COMPROVANTE = 0
