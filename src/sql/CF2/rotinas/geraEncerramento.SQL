UPDATE CF
SET CF.FASE_ID = 9,
    CF.DT_UPDATE = CURRENT_TIMESTAMP
FROM SIC..CONFIRMAFACIL CF
WHERE FASE_ID = 6
AND FLAG_COMPROVANTE = 1

AND  EXISTS (SELECT 1 FROM SIC..CONFIRMAFACILOCORRENCIA CO 
             WHERE CO.CONFIRMAFACIL_ID = CF.ID AND CO.OCORRENCIA_ID = 1 AND CO.FLAG_SEND=1)  -- EXISTE 001 ENVIADO

AND  EXISTS (SELECT 1 FROM SIC..CONFIRMAFACILOCORRENCIA CO 
             WHERE CO.CONFIRMAFACIL_ID = CF.ID AND CO.OCORRENCIA_ID = 999 
               AND ( CO.FLAG_SEND=1 OR DATEDIFF(DAY,CO.DT_SEND, CURRENT_TIMESTAMP) > 5) -- EXISTE 999 EVIADO OU COM MAIS DE 4 DIAS
             )