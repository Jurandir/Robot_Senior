BEGIN
    UPDATE CF
    SET DT_EMBARQUE = CNH.DATAEMBARQUE
    FROM SIC.dbo.CONFIRMAFACIL CF
    JOIN CARGASSQL.dbo.CNH        ON CNH.EMP_CODIGO = SUBSTRING(CF.CTRC,1,3) AND 
                                     CNH.SERIE      = SUBSTRING(CF.CTRC,4,1)	AND 
                                     CNH.CTRC       = SUBSTRING(CF.CTRC,5,10)
    WHERE
    DT_EMBARQUE IS NULL AND CNH.DATAEMBARQUE IS NOT NULL AND CNH.VALORNF>0
END   
;
BEGIN TRY
    BEGIN TRANSACTION

    BEGIN
        ;
        INSERT INTO SIC.dbo.CONFIRMAFACILOCORRENCIA
          ( CONFIRMAFACIL_ID, OCORRENCIA_ID, OCORRENCIA_NOME, OCORRENCIA_DATA )
        SELECT
          CF.ID                                           AS CONFIRMAFACIL_ID,
          128                                             AS OCORRENCIA_ID,
          'EM PROCESSO DE TRANSFERENCIA ENTRE AS FILIAIS' AS OCORRENCIA_NOME,
          CF.DT_EMBARQUE                                  AS OCORRENCIA_DATA
        FROM SIC.dbo.CONFIRMAFACIL CF
          JOIN CARGASSQL.dbo.CNH ON CNH.EMP_CODIGO=SUBSTRING(CF.CTRC,1,3) AND CNH.SERIE = SUBSTRING(CF.CTRC,4,1) AND CNH.CTRC = SUBSTRING(CF.CTRC,5,10)
        WHERE FASE_ID = 1 AND CF.DT_EMBARQUE IS NOT NULL
          AND NOT EXISTS ( SELECT 1 FROM SIC.dbo.CONFIRMAFACILOCORRENCIA CO WHERE CO.CONFIRMAFACIL_ID = CF.ID AND CO.OCORRENCIA_ID = 128 ) 
        -- CASO A (FASE = 1) E  NÃƒO EXISTIR OCORRENCIA = 128 GERADA, INCLUI OCORRENCIA

        ;
        UPDATE CF
          SET FASE_ID = 2,
              DT_UPDATE = CURRENT_TIMESTAMP
         FROM SIC.dbo.CONFIRMAFACIL CF
        WHERE FASE_ID = 1 AND DT_EMBARQUE IS NOT NULL
          AND EXISTS ( SELECT 1 FROM SIC.dbo.CONFIRMAFACILOCORRENCIA CO WHERE CO.CONFIRMAFACIL_ID = CF.ID AND CO.OCORRENCIA_ID = 128 ) 
        -- CASO A (FASE = 1) E EXISTIR OCORRENCIA = 128 GERADA, MUDA PARA FASE = 2

        ;
    END

    COMMIT TRANSACTION

END TRY
BEGIN CATCH
    IF @@TRANCOUNT > 0
        ROLLBACK TRANSACTION

    DECLARE @ErrorMessage NVARCHAR(4000) = ERROR_MESSAGE()
    DECLARE @ErrorSeverity INT = ERROR_SEVERITY()
    DECLARE @ErrorState INT = ERROR_STATE()

    RAISERROR (@ErrorMessage, @ErrorSeverity, @ErrorState);
END CATCH
;
