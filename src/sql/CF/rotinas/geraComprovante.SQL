SET XACT_ABORT ON

BEGIN TRY
    BEGIN TRANSACTION

        ;
        INSERT INTO SIC.dbo.CONFIRMAFACILOCORRENCIA
          ( CONFIRMAFACIL_ID, OCORRENCIA_ID, OCORRENCIA_NOME, OCORRENCIA_DATA )
        SELECT
          CF.ID                             AS CONFIRMAFACIL_ID,
          999                               AS OCORRENCIA_ID,
          'COMPROVANTE DE ENTREGA'          AS OCORRENCIA_NOME,
          CO.OCORRENCIA_DATA                AS OCORRENCIA_DATA
        FROM SIC.dbo.CONFIRMAFACIL CF
		    JOIN SIC.dbo.CONFIRMAFACILOCORRENCIA CO ON CO.CONFIRMAFACIL_ID = CF.ID AND CO.OCORRENCIA_ID = 1 AND CO.FLAG_SEND = 1
        WHERE FASE_ID = 5
        -- CASO A (FASE = 5) E  EXISTIR OCORRENCIA = 1 GERADA E ENVIADA, INCLUI OCORRENCIA
        ;
        UPDATE CF 
          SET FASE_ID = 6,
              DT_UPDATE = CURRENT_TIMESTAMP
        FROM  SIC.dbo.CONFIRMAFACIL CF
        WHERE FASE_ID = 5
          AND EXISTS ( SELECT 1 FROM SIC.dbo.CONFIRMAFACILOCORRENCIA CO WHERE CO.CONFIRMAFACIL_ID = CF.ID AND CO.OCORRENCIA_ID = 1 AND CO.FLAG_SEND = 1 ) 
        -- CASO A (FASE = 5) E EXISTIR OCORRENCIA = 1 GERADA E ENVIADA, MUDA PARA FASE = 6
        ;

    COMMIT TRANSACTION

END TRY
BEGIN CATCH
    IF @@TRANCOUNT > 0
        ROLLBACK TRANSACTION

    DECLARE @ErrorMessage NVARCHAR(4000) = ERROR_MESSAGE()
    DECLARE @ErrorSeverity INT = ERROR_SEVERITY()
    DECLARE @ErrorState INT = ERROR_STATE()

    RAISERROR (@ErrorMessage, @ErrorSeverity, @ErrorState);
END CATCH
;